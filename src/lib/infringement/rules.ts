function normalizeTerm(term: string) {
  return term.toLowerCase().replace(/\s+/g, " ").trim();
}

function dedupeTerms(terms: string[]) {
  const seen = new Set<string>();
  const result: string[] = [];

  for (const term of terms) {
    const normalized = normalizeTerm(term);
    if (!normalized || seen.has(normalized)) continue;
    seen.add(normalized);
    result.push(term.trim());
  }

  return result;
}

function expandWithContexts(roots: string[], contexts: string[]) {
  return roots.flatMap((root) => contexts.map((context) => `${root} ${context}`));
}

function buildProtectedTerms(roots: string[], extraContexts: string[] = []) {
  return dedupeTerms([
    ...roots,
    ...expandWithContexts(roots, apparelContexts),
    ...expandWithContexts(roots, riskyListingContexts),
    ...expandWithContexts(roots, visualMarkContexts),
    ...expandWithContexts(roots, extraContexts),
  ]);
}

function createRuleEntry(rule: InfringementRule, term: string, index: number): InfringementRuleEntry {
  return {
    category: rule.category,
    descriptionEn: rule.descriptionEn,
    descriptionZh: rule.descriptionZh,
    id: `${rule.id}-${index + 1}`,
    labelEn: rule.labelEn,
    labelZh: rule.labelZh,
    policyBasisEn: rule.policyBasisEn,
    policyBasisZh: rule.policyBasisZh,
    ruleId: rule.id,
    severity: rule.severity,
    sourceLabel: rule.sourceLabel,
    sourceUrl: rule.sourceUrl,
    term,
  };
}

