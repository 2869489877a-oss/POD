function createEmptyCategoryStats() {
  return {
    brand: 0,
    celebrity: 0,
    character: 0,
    copyright_phrase: 0,
    logo: 0,
    marketplace: 0,
    sports: 0,
    visual_review: 0,
  } satisfies Record<InfringementRuleCategory, number>;
}

function createEmptySeverityStats() {
  return {
    critical: 0,
    high: 0,
    low: 0,
    medium: 0,
  } satisfies Record<InfringementSeverity, number>;
}

export const infringementRuleStats = infringementRuleEntries.reduce(
  (stats, entry) => {
    stats.byCategory[entry.category] += 1;
    stats.bySeverity[entry.severity] += 1;
    return stats;
  },
  {
    byCategory: createEmptyCategoryStats(),
    bySeverity: createEmptySeverityStats(),
    sourceCount: new Set(infringementRules.map((rule) => rule.sourceUrl).filter(Boolean)).size,
    totalRules: infringementRules.length,
    totalTerms: infringementRuleEntries.length,
    version: RULE_ENGINE_VERSION,
  },
);
